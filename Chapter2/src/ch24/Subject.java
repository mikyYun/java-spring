package ch24;

public class Subject {

	private String name;
	private int score;
	
	public Subject() {}
	
	public String getName() {
		return name;
	}
	public int getScore() {
		return score;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	public void setScore(int score) {
		this.score = score;
	}
	
}
